def f(D):
	print("ans = 0")
	for x in range(0, D+1):
		s = 'f({})'.format(x)
		p = 1
		for a in range(0, D+1):
			if a != x:
				s += "*(n-" + str(a) + ")"
				p *= (x - a)
		s += '//('+ str(p) +')'
		print("ans += " + s)
	print("ans %= MOD")
	print("----------------")

for d in range(2, 10):
	f(d)


